        private void SetCorners(ref bool[,] board, int width, int height, bool value)
        {
            board[0, 0] = value;
            board[0, height - 1] = value;
            board[width - 1, 0] = value;
            board[width - 1, height - 1] = value;
        }

        private int CountAlive(bool[,] board, int width, int height)
        {
            int count = 0;
            for (int y = 0; y < height; y++)
            {
                for (int x = 0; x < width; x++)
                {
                    if (board[x, y])
                    {
                        count++;
                    }
                }
            }
            return count;
        }

        private bool[,] NextBoard(bool[,] board, int width, int height)
        {
            var nextBoard = new bool[width, height];
            for (int y = 0; y < height; y++)
            {
                for (int x = 0; x < width; x++)
                {
                    int count = CountSurrounding(board, width, height, x, y);

                    if (board[x, y])
                    {
                        if (count == 2 || count == 3)
                        {
                            nextBoard[x, y] = true;
                        }
                        else
                        {
                            nextBoard[x, y] = false;
                        }
                    }
                    else
                    {
                        if (count == 3)
                        {
                            nextBoard[x, y] = true;
                        }
                        else
                        {
                            nextBoard[x, y] = false;
                        }
                    }
                }
            }

            return nextBoard;
        }

        private static int[,] SurroundingCoords =
        {
            //all coords on the left
            {-1, -1},
            {-1,  0},
            {-1,  1},

            //middle
            { 0, -1},
            { 0,  1},

            //all coords on the right
            { 1, -1},
            { 1,  0},
            { 1,  1},
        };


        private int CountSurrounding(bool[,] board, int width, int height, int x, int y)
        {
            int count = 0;
            for (int i = 0; i < SurroundingCoords.GetLength(0); i++)
            {
                int surroundingX = x + SurroundingCoords[i, 0];
                int surroundingY = y + SurroundingCoords[i, 1];
                if (
                    //Check coords
                    surroundingX >= 0 && surroundingX < width &&
                    surroundingY >= 0 && surroundingY < height &&
                    board[surroundingX, surroundingY]
                    )
                {
                    count++;
                }
            }
            return count;
        }

        private bool[,] ParseInput(string input, int width, int height)
        {
            //Debug.WriteLine("parsing.");
            var result = new bool[width, height];
            var lines = input.Split(new string[] { "\r\n" }, StringSplitOptions.None);
            for (int y = 0; y < height; y++)
            {
                for (int x = 0; x < width; x++)
                {
                    result[x, y] = lines[y][x] == '#';
                }
            }

            return result;
        }

        private void PrintBoard(bool[,] board, int width, int height)
        {
            Debug.WriteLine("===============");

            for (int y = 0; y < height; y++)
            {
                for (int x = 0; x < width; x++)
                {
                    Debug.Write(board[x, y] ? '#' : '.');
                }
                Debug.Write('\n');
            }
        }


